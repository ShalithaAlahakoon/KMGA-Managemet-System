package model;

public class LocationEquipment {
	
	private String mapLetter;
	private String eqName;
	
	
	public LocationEquipment(String mapLetter, String eqName) {
		super();
		this.mapLetter = mapLetter;
		this.eqName = eqName;
	}


	
	public String getMapLetter() {
		return mapLetter;
	}


	public String getEqName() {
		return eqName;
	}


	

}
